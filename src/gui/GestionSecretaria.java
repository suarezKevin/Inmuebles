    public void listarInquilinos() {
        try {
            hallarPropietario();
            Conexion cc = new Conexion();
            Connection cn = cc.conectar();
            String[] titulos = {"Cedula", "Nombre", "Apellido", "Telefono", "Direccion"};
            this.modelo = new DefaultTableModel(null, titulos);
            String[] registros = new String[5];
            String sql = "";
            sql = "select i.ced_inq, i.nom_inq, i.ape_inq, i.tel_inq, i.dir_inq from inquilinos i, inmuebles inm, alquilar a, propietarios p where i.ced_inq=a.ced_inquilino and a.id_inm_alq=inm.id_inm and inm.ced_pro_per='" + this.cedulaPropietario + "' and a.estado_alq='YES' GROUP BY a.ced_inquilino";
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("i.ced_inq");
                registros[1] = rs.getString("i.nom_inq");
                registros[2] = rs.getString("i.ape_inq");
                registros[3] = rs.getString("i.tel_inq");
                registros[4] = rs.getString("i.dir_inq");
                this.modelo.addRow(registros);
            }
            jTblInquilinos.setModel(this.modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    public void hallarPropietario() {
        try {
            Conexion cc = new Conexion();
            Connection cn = cc.conectar();
            String sql = "";
            sql = "select p.ced_pro from propietarios p, secretarias s, contrato_pro_sec c where p.ced_pro=c.ced_pro_con and c.ced_sec_con='" + this.cedula + "' and c.estado_contrato='YES' Group BY '" + this.cedula + "'";
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while (rs.next()) {
                this.cedulaPropietario = rs.getString("p.ced_pro");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

